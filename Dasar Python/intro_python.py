# Introduction
print(2 + 3)   # addition(+)                    |penjumlahan|
print(3 - 1)   # subtraction(-)                 |pengurangan|
print(2 * 3)   # multiplication(*)              |perkalian|
print(3 / 2)   # division(/)                    |pembagian|
print(3 ** 2)  # exponential(**)                |perpangkatan|
print(3 % 2)   # modulus(%)                     |sisa bagi|
print(3 // 2)  # Floor division operator(//)    |pembulatan hasil bagi|

# Tipe Data Pada Python
